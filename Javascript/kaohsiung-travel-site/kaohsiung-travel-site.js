// 指定 dom
var AR = document.querySelector('#ARId');
var hotAR = document.querySelector('#hotARList');
var listTitle = document.querySelector('#listTitle');
var list = document.querySelector('#list');

// 監聽與更新
areaUpdated(area);
updatedListOriginal();
AR.addEventListener('change', updatedList);
hotAR.addEventListener('click', hotarea);

//行政區下拉選單
function areaUpdated(items) {
    var str = '';
    for (var i = 0; i < items.length; i++) {
        str += '<option data-number="' + i + '" value="' + items[i] + '">' + items[i] + '</option>'
    }
    AR.innerHTML = str;
}
//熱門行政區
function hotarea(e) {
    if (e.target.nodeName !== 'INPUT') {
        return;
    }
    // console.log(e.target.value);
    updatedList(e);
}

//同步更新網頁
function updatedList(e) {
    var str = '';
    for (var i = 0; i < data.length; i++) {
        if (data[i].Zone == e.target.value) {
            listTitle.textContent = data[i].Zone;
            str += '<li class="list-box"><div class="list-img " style="background:url(' + data[i].Picture1 + ');background-size: cover;background-position: center; "><h3>' + data[i].Name + '</h3><h2>' + data[i].Zone + '</h2></div><ul><li ><img src="https://raw.githubusercontent.com/00qage/travel/master/images/icons_clock.png" alt="clock">' + data[i].Opentime + '</li><li ><img src="https://raw.githubusercontent.com/00qage/travel/master/images/icons_pin.png" alt="pin">' + data[i].Add + '</li><li class="left-li"><img src="https://raw.githubusercontent.com/00qage/travel/master/images/icons_phone.png" alt="phone">' + data[i].Tel + '</li><li class="right-li"><img src="images/icons_tag.png" alt="tag">' + data[i].Ticketinfo + '</li></ul></li>'
        }
        list.innerHTML = str;
    }
}

